import { useScroll, useTransform, useSpring, MotionValue, Variants } from "framer-motion";
import { useRef } from "react";

interface ScrollAnimationOptions {
  offset?: ["start end", "end start"] | ["start end", "center center"];
  smooth?: boolean;
}

export const useScrollAnimation = (options: ScrollAnimationOptions = {}) => {
  const ref = useRef<HTMLDivElement>(null);
  const { scrollYProgress } = useScroll({
    target: ref,
    offset: options.offset || ["start end", "end start"],
  });

  const smoothProgress = useSpring(scrollYProgress, {
    stiffness: 100,
    damping: 30,
    restDelta: 0.001,
  });

  return {
    ref,
    scrollYProgress: options.smooth ? smoothProgress : scrollYProgress,
  };
};

export const useParallax = (value: MotionValue<number>, distance: number) => {
  return useTransform(value, [0, 1], [-distance, distance]);
};

